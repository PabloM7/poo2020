    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        // TODO add your handling code here:
        String nombre_buscar = buscar.getText();
        lblnombre.setText("");
        for(Producto p: Registro.productos){
            //Comparacion del nombre con el de los arreglos
            if(p.getNombre().contains(nombre_buscar)){
                lblnombre.setText(p.getNombre());
                lblmedida.setText(p.getMedida());
                lblstock.setText(String.valueOf(p.getCantidad()));
                lblprecio.setText(String.valueOf(p.getPrecio()));
            }
        }
    }//GEN-LAST:event_buscarActionPerformed

    private void facturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_facturaActionPerformed
        // TODO add your handling code here:
        cantidad = Integer.parseInt(lblcantidad.getText());
        precio = Double.parseDouble(lblprecio.getText());
        deposito = Integer.parseInt(lblstock.getText());
        lblsubtotal.setText("0");
        
        if(cantidad <= deposito){
            sub_total = (cantidad * precio);
            deposito-=cantidad;
             
        }else{
            reporte = "Existencias de producto insuficientes";
            insuficiente.setText(String.valueOf(reporte));
        }
          
        iva = sub_total*(0.12);   
        total = sub_total+iva;
        tot +=total;
        lblsubtotal.setText(String.valueOf(sub_total));
        lbliva.setText(String.valueOf(iva));
        lbltotal.setText(String.valueOf(total));
    }//GEN-LAST:event_facturaActionPerformed

