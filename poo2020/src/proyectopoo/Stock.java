    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        nombre = txtnombre.getText();
        medida = txtmedida.getText();
        int cantidad = Integer.parseInt(txtcantidad.getText());
        setPrecio(Double.parseDouble(txtprecio.getText()));
        //Creamos un objeto de tipo producto
        Producto producto = new Producto(nombre,medida,cantidad, getPrecio());
        //Agregamos el producto a nuestra lista de prodcutos
        productos.add(producto);
        Limpiar();
        ver_lista();
    }//GEN-LAST:event_guardarActionPerformed

      public void ver_lista(){
          String [] prodNombres = new String[productos.size()];
          int cont =0;
          for(Producto producto : productos){
              prodNombres[cont]= producto.getNombre();
              cont++;          
          }
        
          lista.setListData(prodNombres);
      }
      public void Limpiar(){
          txtnombre.setText("");
          txtmedida.setText("");
          txtcantidad.setText("");
          txtprecio.setText("");
      }
